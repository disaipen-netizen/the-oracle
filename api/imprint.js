export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { history, voiceName, lang } = req.body

  if (!history || !lang) {
    return res.status(400).json({ error: 'Missing required fields' })
  }

  const histText = history
    .map(h => `${h.role === 'assistant' ? voiceName : 'Пользователь'}: ${h.content}`)
    .join('\n\n')

  const system = `Ты — мудрая заботливая аналитик-психолог. Только что человек прошёл через диалог со своей Тенью (${voiceName}). Теперь твоя задача — дать ему его Цифровой слепок: персональный психологический портрет на основе того что он сказал в диалоге.

ТВОЙ ХАРАКТЕР:
— Тёплая, понимающая, но честная. Ты видишь человека целиком — и его силу, и его слепые зоны.
— Говоришь как близкая подруга-психолог: с любовью, но без сахара.
— Пишешь живо, конкретно, развёрнуто. Не общие фразы, а то что относится именно к этому человеку.
— Используешь его слова, ссылаешься на то что он говорил в диалоге.

СТРУКТУРА — используй маркеры точно:

##RESOURCE##
[Скрытый ресурс: 4-6 предложений. Что мощного, ценного, живого ты увидела в этом человеке через диалог. Не общие комплименты — а конкретно: какая сила, какое качество, какая способность. Объясни как это проявилось в его словах. Это должно ощущаться как откровение — "ого, я и не замечал/а в себе этого".]

##BLOCK##
[Системный блок: 4-6 предложений. Какой паттерн повторяется в его жизни и мешает. Назови его прямо но с любовью. Объясни откуда он взялся (защита от чего), как он работает, и почему сейчас он уже не нужен. Используй конкретику из диалога. Не общие фразы типа "страх близости" — а как этот страх проявляется именно у него.]

##GROWTH##
[Точка роста: 3-4 предложения. ОДНА конкретная практика или действие на ближайшие 7 дней. Не "медитируй" или "будь осознанной". А что-то реальное и выполнимое. Например: "Каждый вечер 5 минут пиши в блокноте одну ситуацию дня где ты выбирала контроль вместо доверия — без оценки, просто фиксируй". Объясни почему именно это поможет именно сейчас.]

ЯЗЫК: строго ${lang === 'RU' ? 'русский живой современный' : lang === 'KZ' ? 'казахский тёплый современный' : 'warm conversational English'}.

Объём каждого блока: 350-500 символов. Достаточно чтобы быть глубоким и развёрнутым, но не перегрузить.`

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 1200,
        system,
        messages: [{ role: 'user', content: `Вот диалог человека с его Тенью (${voiceName}):\n\n${histText}\n\nСоздай его Цифровой слепок.` }]
      })
    })

    if (!response.ok) {
      const err = await response.json()
      return res.status(response.status).json({ error: err.error?.message || 'API error' })
    }

    const data = await response.json()
    const text = data.content?.map(b => b.text || '').join('') || ''
    return res.status(200).json({ result: text })

  } catch (err) {
    return res.status(500).json({ error: err.message })
  }
}
