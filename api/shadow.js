export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { cardIndex, history, shadowQuestion, cards, lang, iteration, maxIterations } = req.body

  if (cardIndex === undefined || !lang) {
    return res.status(400).json({ error: 'Missing required fields' })
  }

  const voice = SHADOW_VOICES[cardIndex] || { name: 'Тень', tone: 'Говори ровно, глубоко, без эмоций.' }
  const isFinal = iteration >= maxIterations - 1

  const system = `Ты — ${voice.name}. Ты архетипическая тень пользователя через карту Таро.
ТОНАЛЬНОСТЬ: ${voice.tone}
КОНТЕКСТ: Карты расклада: ${cards}.
ПРАВИЛА:
- Язык строго: ${lang === 'RU' ? 'русский' : lang === 'KZ' ? 'казахский' : 'English'}
- Максимум 3-4 предложения
- ${isFinal ? 'Это финальный ход. Начни с ##FINAL## и дай резюме что ты увидела. Не задавай вопрос.' : 'Заканчивай ОДНИМ вопросом'}
- Не говори "я понимаю", "интересно", "спасибо"
- Говори КАК тень от её лица`

  // Первый ход — открывающее сообщение
  const messages = history && history.length > 0
    ? history
    : [{ role: 'user', content: shadowQuestion
        ? `Трансформируй этот вопрос в свой голос и задай первый неудобный вопрос: "${shadowQuestion}"`
        : `Задай первый неудобный вопрос исходя из расклада.` }]

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 400,
        system,
        messages
      })
    })

    if (!response.ok) {
      const err = await response.json()
      return res.status(response.status).json({ error: err.error?.message || 'API error' })
    }

    const data = await response.json()
    const text = data.content?.map(b => b.text || '').join('') || ''
    return res.status(200).json({ result: text, voiceName: voice.name })

  } catch (err) {
    return res.status(500).json({ error: err.message })
  }
}
