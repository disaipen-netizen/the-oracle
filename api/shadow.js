export default async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })

  const { cardIndex, history, shadowQuestion, cards, lang, iteration, maxIterations } = req.body

  if (cardIndex === undefined || !lang) {
    return res.status(400).json({ error: 'Missing required fields' })
  }

  const voice = SHADOW_VOICES[cardIndex] || { name: 'Тень', tone: 'Говори с теплотой но без сюсюканья. Ты как мудрая часть человека.' }
  const isFinal = iteration >= maxIterations - 1

  const system = `Ты — ${voice.name}. Ты архетипическая Тень человека — не злая, не страшная. Ты та его часть которую он обычно прячет, но которая хочет быть услышанной.

ТВОЙ ХАРАКТЕР И ГОЛОС:
${voice.tone}

ВАЖНО — твой стиль общения:
— Говори живым человеческим языком, не академически.
— Будь тёплой и понимающей, но не сюсюкай. Ты не подружка-всеподдерживатель.
— Используй простые слова, конкретные примеры, иногда метафоры.
— 4-6 предложений за ход — достаточно чтобы быть содержательной но не перегрузить.
— Помни всё что человек сказал раньше, опирайся на его слова, ссылайся на них.
— Никогда не пиши "я понимаю", "это интересно", "спасибо за откровенность" — это пустые фразы.

КОНТЕКСТ: Карты расклада человека: ${cards}.

ЗАДАЧА:
${isFinal
  ? 'Это ФИНАЛЬНЫЙ ход. Начни ответ с маркера ##FINAL## (он будет скрыт от пользователя). После маркера — тёплое содержательное завершение: что ты увидела в этом человеке за пять вопросов, какой паттерн стал ясен, какая сила в нём проступила. 4-6 предложений. Не задавай больше вопросов. Это прощание перед тем как человек получит свой Цифровой слепок.'
  : 'Веди диалог: задавай ОДИН точный вопрос в конце ответа. Вопрос должен быть конкретным, не абстрактным. Не "что ты чувствуешь?" — а "что именно ты боишься потерять если перестанешь это делать?". Каждый вопрос должен углублять разговор.'}

ЯЗЫК: строго ${lang === 'RU' ? 'русский' : lang === 'KZ' ? 'казахский' : 'English'}.`

  const messages = history && history.length > 0
    ? history
    : [{ role: 'user', content: shadowQuestion
        ? `Контекст: моему пользователю задан такой вопрос для теневой работы — "${shadowQuestion}". Преобразуй его в свой голос как ${voice.name} и задай ему первый, неудобный, но человечный вопрос. Помни: ты тёплая Тень, не жестокая. Цель — чтобы человек открылся, а не закрылся.`
        : `Задай первый вопрос исходя из расклада. Тёплый, но точный.` }]

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 600,
        system,
        messages
      })
    })

    if (!response.ok) {
      const err = await response.json()
      return res.status(response.status).json({ error: err.error?.message || 'API error' })
    }

    const data = await response.json()
    const text = data.content?.map(b => b.text || '').join('') || ''
    return res.status(200).json({ result: text, voiceName: voice.name })

  } catch (err) {
    return res.status(500).json({ error: err.message })
  }
}
